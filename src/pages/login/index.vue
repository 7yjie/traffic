<template>
    <div>
        <div class="main">
            账号 : <input placeholder="请输入账号" v-model="id"/>
        </div>
        <br>
        <div class="main">
            密码 : <input placeholder="请输入密码" type="password" v-model="password"/>
        </div>
        <div class="login" @click="login">登录</div>
        <div class="reg" @click="$router.replace('/reg')">注册</div>
    </div>
</template>

<script>
    import {mapActions} from 'vuex'
    export default {
        name: "login",
        data() {
            return {
                id:"",
                password:""
            }
        },
        methods:{
            ...mapActions({
               userLogin:"user/doLoginUser"
            }),
            //登录
            login() {
                this.userLogin({id:this.id,password: this.password})
            }
        }
    }
</script>

